public class HttpProtocol {
    public void connectHttp() {
        System.out.println("Идет соединение по протоколу Http...");
    }

    public void disconnectHttp() {
        System.out.println("Разрыв соединения протокола Http");
    }

    public void updateConnectionHttp() {
        System.out.println("Обновление соединения по протоколу Http...");
    }
}
